// Helper function to find staff by name slug
export const findStaffByNameSlug = (nameSlug) => {
  const allStaff = getAllStaff();
  return allStaff.find(person => 
    person.name.toLowerCase().replace(/\s+/g, '-') === nameSlug
  );
};